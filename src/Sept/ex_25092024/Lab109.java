package Sept.ex_25092024;

public class Lab109 {
    public static void main(String[] args) {
        //both for loop are same
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        System.out.println("-------");
        for (int i = 0; i < 10; ++i) {
            System.out.println(i);
        }
        for (char i = 'A'; i <= 'Z'; i++) {
            System.out.println(i);
        }
    }
}
